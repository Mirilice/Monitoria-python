# Escreva um programa que leia 3 números inteiros, calcule e
# escreva a média aritmética entre eles.

num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))
num3 = int(input("Digite o terceiro número: "))

media = (num1 + num2 + num3) / 3

print(f"A média aritmética entre {num1}, {num2} e {num3} é {media}.")